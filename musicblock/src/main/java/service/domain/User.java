package service.domain;

public class User {
	private	int uCode;		// User Code
	private String nick;	// Nickname

	public int getUCode() {
		return uCode;
	}

	public void setUCode(int uCode) {
		this.uCode = uCode;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}
}
